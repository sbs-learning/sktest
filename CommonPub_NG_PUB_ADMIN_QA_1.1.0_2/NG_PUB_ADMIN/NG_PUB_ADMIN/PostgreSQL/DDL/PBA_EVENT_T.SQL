CREATE TABLE pub_admin.pba_event_t
(
/* ----------------------------------------------------------------------------------
      -- Author  : GF8561
      -- Created : 22/04/2021
      -- Purpose : This table is used to keep record of the Event of statements of any process.
      -----------------------------------------------------------------------------------
      -- Modification History
      -----------------------------------------------------------------------------------
      -- Ref#   Version#  Name                JIRA#    Date           Change Description
      -----------------------------------------------------------------------------------
      --  1.              Kalyan Kumar                22/04/2021      Initial Version
    ----------------------------------------------------------------------------------- */
	
	event_id				INTEGER GENERATED BY DEFAULT AS IDENTITY,
	event_name				VARCHAR(256),
	table_name				VARCHAR(256),
	event_src_cd_location	VARCHAR(250),
	event_statement			TEXT,
	event_dtl				VARCHAR(4000),
	rcrd_create_user_id		VARCHAR(30) NOT NULL,
	rcrd_create_ip			VARCHAR(30) NOT NULL,
	rcrd_create_ts			TIMESTAMPTZ NOT NULL,
	CONSTRAINT pba_event_id_pk PRIMARY KEY (event_id)
);

   COMMENT ON COLUMN "pub_admin"."pba_event_t"."event_id" IS 'System-generated identifier for the Event';
   COMMENT ON COLUMN "pub_admin"."pba_event_t"."event_name" IS 'Name of the Event that occurred.';
   COMMENT ON COLUMN "pub_admin"."pba_event_t"."table_name" IS 'Name of the table on which event occurred.';
   COMMENT ON COLUMN "pub_admin"."pba_event_t"."event_src_cd_location" IS 'Describes the source of the event: Procedure, Function etc.';
   COMMENT ON COLUMN "pub_admin"."pba_event_t"."event_statement" IS 'Statement of Event that had occured.';
   COMMENT ON COLUMN "pub_admin"."pba_event_t"."event_dtl" IS 'Additional detail information about the event, as provided by the component reporting it.';
   COMMENT ON COLUMN "pub_admin"."pba_event_t"."rcrd_create_user_id" IS 'User who inserted this row.';
   COMMENT ON COLUMN "pub_admin"."pba_event_t"."rcrd_create_ip" IS 'IP of the machine from where the row was inserted.';
   COMMENT ON COLUMN "pub_admin"."pba_event_t"."rcrd_create_ts" IS 'Date/time the row was inserted.';
   COMMENT ON TABLE "pub_admin"."pba_event_t"  IS 'EVENT
An occurrence of some type (such as an error, business rule violation, or routine event) that is reported by a process step.';
