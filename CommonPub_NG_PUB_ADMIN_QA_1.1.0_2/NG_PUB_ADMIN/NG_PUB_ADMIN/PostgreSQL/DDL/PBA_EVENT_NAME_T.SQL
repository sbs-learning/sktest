CREATE TABLE pub_admin.pba_event_name_t
(
/* ----------------------------------------------------------------------------------
      -- Author  : GF8561
      -- Created : 12/05/2021
      -- Purpose : This table is used to keep record of the Event of statements of any process.
      -----------------------------------------------------------------------------------
      -- Modification History
      -----------------------------------------------------------------------------------
      -- Ref#   Version#  Name                JIRA#    Date           Change Description
      -----------------------------------------------------------------------------------
      --  1.              Kalyan Kumar                12/05/2021      Initial Version
    ----------------------------------------------------------------------------------- */
	event_name_id		INTEGER GENERATED BY DEFAULT AS IDENTITY,
	event_constant		CHARACTER VARYING(100) NOT NULL,
	event_name			CHARACTER VARYING(256) NOT NULL,
	event_name_desc		CHARACTER VARYING(1000),
	rcrd_create_user_id	CHARACTER VARYING(30) NOT NULL,
	rcrd_create_ip		VARCHAR(30) NOT NULL,
	rcrd_create_ts		TIMESTAMPTZ NOT NULL,
	rcrd_updt_user_id	CHARACTER VARYING(30),
	rcrd_updt_ip		VARCHAR(30),
	rcrd_updt_ts		TIMESTAMPTZ,
	CONSTRAINT pba_event_name_id_pk PRIMARY KEY (event_name_id),
	CONSTRAINT pba_event_name_nk UNIQUE (event_name),
	CONSTRAINT pba_event_constant_nk UNIQUE (event_constant)
);

   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."event_name_id" IS 'System-generated identifier for the Event';
   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."event_constant" IS 'Constant used to Define Event Process.';
   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."event_name" IS 'Name of the event for which event occurred.';
   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."event_name_desc" IS 'Describes the source of the event: Procedure, Function etc.';
   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."rcrd_create_user_id" IS 'User who inserted this row.';
   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."rcrd_create_ip" IS 'IP of the machine from where the row was inserted.';
   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."rcrd_create_ts" IS 'Date/time the row was inserted.';
   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."rcrd_updt_user_id" IS 'User who updated this row.';
   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."rcrd_updt_ip" IS 'IP of the machine from where the row was updated.';
   COMMENT ON COLUMN "pub_admin"."pba_event_name_t"."rcrd_updt_ts" IS 'Date/time the row was updated.';
   COMMENT ON TABLE "pub_admin"."pba_event_name_t"  IS 'EVENT NAME EVENT Name that is used to uniquely identify that Process';