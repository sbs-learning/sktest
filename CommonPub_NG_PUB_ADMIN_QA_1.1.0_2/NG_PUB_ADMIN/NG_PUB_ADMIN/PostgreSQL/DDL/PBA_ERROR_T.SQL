CREATE TABLE pub_admin.pba_error_t
(
/* ----------------------------------------------------------------------------------
      -- Author  : GF8561
      -- Created : 22/04/2021
      -- Purpose : This table is used to keep error records that is generated in any process.
      -----------------------------------------------------------------------------------
      -- Modification History
      -----------------------------------------------------------------------------------
      -- Ref#   Version#  Name                JIRA#    Date           Change Description
      -----------------------------------------------------------------------------------
      --  1.              Kalyan Kumar                22/04/2021      Initial Version
    ----------------------------------------------------------------------------------- */
	err_id serial NOT NULL,
	table_name CHARACTER VARYING(256),
	err_ts TIMESTAMP WITHOUT TIME ZONE,
	system_err_cd CHARACTER VARYING(100),
	system_err_msg TEXT,
	stack_trace_details TEXT,
	user_err_msg CHARACTER VARYING(4000),
	rcrd_create_ts timestamptz NOT NULL,
	rcrd_create_ip VARCHAR(30) NOT NULL,
	rcrd_create_user_id VARCHAR(30) NOT NULL
);
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."err_id" IS 'System-generated identifier for the Error';
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."table_name" IS 'Tbale name on which any Error has occured.';
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."err_ts" IS 'Name of the event for which event occurred.';
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."system_err_cd" IS 'Enters the error code generated by System.';
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."system_err_msg" IS 'Enters the error message generated by System.';
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."stack_trace_details" IS 'Track the error details.';
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."user_err_msg" IS 'Enters the error message generated by user.';
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."rcrd_create_ts" IS 'Date/time the row was inserted.';
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."rcrd_create_ip" IS 'IP of the machine from where the row was inserted.';
   COMMENT ON COLUMN "pub_admin"."pba_error_t"."rcrd_create_user_id" IS 'User who inserted this row.';
   COMMENT ON TABLE "pub_admin"."pba_error_t"  IS 'ERROR
Enters Error that occurs in any process';